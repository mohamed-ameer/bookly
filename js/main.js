let searchBtn = document.querySelector('#search-btn')
window.onscroll = function(){
    document.querySelector(' .header .header-1 .search-form').classList.remove('active');
    if(window.scrollY > 80){
        document.querySelector('.header .header-2').classList.add('active')
    }else{
        document.querySelector('.header .header-2').classList.remove('active')
    }
};
searchBtn.addEventListener('click',function(){
    document.querySelector(' .header .header-1 .search-form').classList.toggle('active')
});

let loginBtn = document.querySelector('#login-btn');
let closeLoginBtn = document.querySelector('#close-login-btn');
loginBtn.addEventListener('click',() =>{
    document.querySelector('.login-form-container').classList.add('active')
});
closeLoginBtn.addEventListener('click',() =>{
    document.querySelector('.login-form-container').classList.remove('active')
});
window.onload = () =>{

  if(window.scrollY > 80){
    document.querySelector('.header .header-2').classList.add('active');
  }else{
    document.querySelector('.header .header-2').classList.remove('active');
  }

  fadeOut();

}

function loader(){
  document.querySelector('.loader-container').classList.add('active');
}

function fadeOut(){
  setTimeout(loader, 4000);
}

